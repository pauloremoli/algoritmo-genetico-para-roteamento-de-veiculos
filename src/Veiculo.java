public class Veiculo {
	private int id;
	private int capacidade;

	public Veiculo(int id, int capacidade) {
		super();
		this.id = id;
		this.capacidade = capacidade;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getCapacidade() {
		return capacidade;
	}

	public void setCapacidade(int capacidade) {
		this.capacidade = capacidade;
	}

}
